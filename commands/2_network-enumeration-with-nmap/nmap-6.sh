export IP=10.129.2.48
export IP=10.129.2.47



################################################################################
# MONITORAR QUANTIDADE DE ALERTAS DO IDS
#
# O laboratório possui uma página que informa quantos alertas o IDS recebeu.
# Se atingir o limite, o IP será bloqueado.
################################################################################

curl -s http://$IP/status.php



################################################################################
# SCAN INICIAL (TOP 100 PORTAS)
#
# Primeiro passo recomendado.
#
# Descobre rapidamente quais portas conhecidas estão abertas.
################################################################################

sudo nmap \
-Pn \
-n \
-sS \
--top-ports 100 \
-T2 \
--min-rate 100 \
--stats-every 5s \
$IP



################################################################################
# ENUMERAÇÃO DE SERVIÇOS ENCONTRADOS
#
# Descobrir versão dos serviços usando apenas as portas abertas.
################################################################################

sudo nmap \
-Pn \
-n \
-sV \
-sC \
-p21,22,53,80,110,139,143,445 \
$IP \
-oA enum



################################################################################
# DETECÇÃO DE SISTEMA OPERACIONAL
#
# Primeira questão do laboratório.
################################################################################

sudo nmap \
-Pn \
-O \
$IP



################################################################################
# ACK SCAN
#
# Objetivo:
#
# Verificar comportamento do firewall.
#
################################################################################

sudo nmap \
-Pn \
-n \
-sA \
-p22,80,10001 \
--packet-trace \
$IP



################################################################################
# DNS NSID
#
# Segunda questão.
#
# O DNS TCP estava filtrado.
#
# O DNS UDP respondeu normalmente.
#
################################################################################

sudo nmap \
-Pn \
-n \
-sU \
-p53 \
--script dns-nsid \
$IP



################################################################################
# DNS NSID + VERSION
################################################################################

sudo nmap \
-Pn \
-n \
-sU \
-sV \
-p53 \
--script dns-nsid \
$IP



################################################################################
# ENUMERAÇÃO DOS HOSTS DA SUBREDE
################################################################################

sudo nmap \
-sn \
10.129.2.0/24 \
-oG live_hosts.gnmap



################################################################################
# EXTRAIR IPS VIVOS
################################################################################

grep "Up" live_hosts.gnmap | awk '{print $2}' > live_hosts.txt

cat live_hosts.txt



################################################################################
# BUSCAR DNS TCP
################################################################################

sudo nmap \
-Pn \
-n \
-p53 \
--open \
-iL live_hosts.txt \
-oG dns53.gnmap



################################################################################
# BUSCAR DNS UDP
################################################################################

sudo nmap \
-Pn \
-n \
-sU \
-p53 \
--open \
-iL live_hosts.txt \
-oG dns53_udp.gnmap



################################################################################
# EXTRAIR HOSTS COM DNS
################################################################################

grep "/open/" dns53.gnmap | awk '{print $2}' > dns_hosts.txt

grep "/open/" dns53_udp.gnmap | awk '{print $2}' > dns_hosts_udp.txt



################################################################################
# FULL TCP SCAN
#
# Descobrir TODAS as portas.
################################################################################

sudo nmap \
-Pn \
-n \
-sS \
-p- \
-T3 \
--min-rate 1500 \
--max-retries 1 \
--stats-every 10s \
$IP \
-oA full_ports



################################################################################
# FULL TCP SCAN UTILIZANDO SOURCE PORT 53
#
# Técnica ensinada pela aula.
#
# Firewall confiava em conexões originadas da porta 53.
################################################################################

sudo nmap \
-Pn \
-n \
-sS \
-p- \
--source-port 53 \
-T3 \
--min-rate 1500 \
--max-retries 1 \
--stats-every 5s \
$IP \
-oA full_src53



